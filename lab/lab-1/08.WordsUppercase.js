// Напишете програма, която извлича всички думи от подаден низ и ги преобразува в главни букви. 
// Извлечените думи, написани с главни букви, трябва да бъдат отпечатани на един ред, разделени с ", ".

// Входът е един аргумент - текстът, от който да се извлекат и преобразуват думите.

// Изходът трябва да бъде един ред, съдържащ преобразувания низ.

// Съвети · Може да се наложи да използвате регулярен израз или алтернативно да проверите за всички разделители, 
// които могат да бъдат намерени в изречението (напр. ", ", ", "!", "?" и т.н.).

function extractWords(text) {
    // Намираме всички думи (букви/цифри/подчертавки)
    let words = text.match(/\w+/g);

    // Преобразуваме всяка дума в главни букви
    let upperWords = words.map(w => w.toUpperCase());

    // Извеждаме резултата
    console.log(upperWords.join(", "));
}

// Пример:
extractWords('Hi, how are you?');      
// HI, HOW, ARE, YOU

extractWords('hello world! JS is fun.'); 
// HELLO, WORLD, JS, IS, FUN
