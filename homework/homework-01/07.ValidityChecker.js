// Напишете програма, която получава две точки във формат [x1, y1, x2, y2]. 
// Проверете дали разстоянието между всяка точка и началото на декартовата координатна система (0, 0) е валидно. 
// Разстоянието между две точки се счита за валидно, ако е цяло число.

// В случай че разстоянието е валидно, изпишете "{x1, y1} до {x2, y2} е валидно"

// Ако разстоянието е невалидно, изпишете "{x1, y1} до {x2, y2} е невалидно"

// Редът на сравненията винаги трябва да бъде първо {x1, y1} до {0, 0}, 
// след това {x2, y2} до {0, 0} и накрая {x1, y1} до {x2, y2}.

// Входът се състои от две точки, дадени като масив от числа.

// За всяко сравнение изпишете или „{x1, y1} до {x2, y2} е валидно“, 
// ако разстоянието е валидно, или „{x1, y1} до {x2, y2} е невалидно“, ако е невалидно.

function validityChecker(x1, y1, x2, y2) {
    function isValid(a1, b1, a2, b2) {
        let distance = Math.sqrt((a2 - a1) ** 2 + (b2 - b1) ** 2);
        return Number.isInteger(distance);
    }

    if (isValid(x1, y1, 0, 0)) {
        console.log(`{${x1}, ${y1}} to {0, 0} is valid`);
    } else {
        console.log(`{${x1}, ${y1}} to {0, 0} is invalid`);
    }

    if (isValid(x2, y2, 0, 0)) {
        console.log(`{${x2}, ${y2}} to {0, 0} is valid`);
    } else {
        console.log(`{${x2}, ${y2}} to {0, 0} is invalid`);
    }

    if (isValid(x1, y1, x2, y2)) {
        console.log(`{${x1}, ${y1}} to {${x2}, ${y2}} is valid`);
    } else {
        console.log(`{${x1}, ${y1}} to {${x2}, ${y2}} is invalid`);
    }
}

// Примери:
validityChecker(3, 0, 0, 4);
// {3, 0} to {0, 0} is valid
// {0, 4} to {0, 0} is valid
// {3, 0} to {0, 4} is valid

validityChecker(2, 1, 1, 1);
// {2, 1} to {0, 0} is invalid
// {1, 1} to {0, 0} is invalid
// {2, 1} to {1, 1} is valid
