// Напишете функция, която определя дали водачът е в рамките на ограничението на скоростта. Ще получите скоростта и зоната. Всяка зона има различно ограничение:

// · На магистралата ограничението е 130 км/ч

// · На междущатската магистрала ограничението е 90 км/ч

// · В града ограничението е 50 км/ч

// · В жилищен район ограничението е 20 км/ч

// Ако водачът е в рамките на ограниченията, не би трябвало да има изход. Ако обаче водачът е над ограничението, вашата функция трябва да отпечата тежестта на нарушението.

// За превишаване на скоростта до 20 км/ч над ограничението, трябва да се отпечата „превишаване на скоростта“.

// За превишаване на скоростта до 40 км/ч над ограничението, трябва да се отпечата „превишаване на скоростта“.

// За всичко останало, трябва да се отпечата „безразсъдно шофиране“.

// Входът се предоставя като 2 аргумента (число и низ). Първият елемент е текущата скорост (число), вторият елемент е зоната.


function checkSpeed(speed, area) {
    let limits = {
        'motorway': 130,
        'interstate': 90,
        'city': 50,
        'residential': 20
    };

    let limit = limits[area];

    if (speed <= limit) {
        // Няма изход ако е в рамките на ограничението
        return;
    }

    let difference = speed - limit;

    if (difference <= 20) {
        console.log("speeding");
    } else if (difference <= 40) {
        console.log("excessive speeding");
    } else {
        console.log("reckless driving");
    }
}

// Примери:
checkSpeed(40, 'city');        // няма изход
checkSpeed(21, 'residential'); // speeding
checkSpeed(120, 'interstate'); // excessive speeding
checkSpeed(200, 'motorway');   // reckless driving